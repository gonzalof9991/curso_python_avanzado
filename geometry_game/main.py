import turtle

# Create or canvas instance
my_turtle = turtle.Turtle()

my_turtle.penup()
my_turtle.goto(50, 75)
my_turtle.pendown()
# forward() -> es un metodo que sirve para avanzar
my_turtle.forward(100)
# left() -> ir a la izquiera
my_turtle.left(90)
my_turtle.forward(200)
my_turtle.left(90)
my_turtle.forward(100)
my_turtle.left(90)
my_turtle.forward(200)
# Mantener ventana

turtle.done()
